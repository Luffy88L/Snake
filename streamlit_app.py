def generate_problem():
    a = random.randint(1, 99)
    b = random.randint(1, 99)
    op = random.choice(['+', '-', '*', '/'])

    if op == '+':
        ans = a + b
    elif op == '-':
        ans = a - b
    elif op == '*':
        ans = a * b
    else:
        ans = round(a / b, 2)

    return a, op, b, ans

